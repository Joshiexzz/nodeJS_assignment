let a = 10, b = 3;
console.log(a + b); // 13
console.log(a - b); // 7
console.log(a * b); // 30
console.log(a / b); // 3.3333
console.log(a % b); // 1
a++;
console.log(a); // 11

let x = 10, y = 5;
console.log(x == y);  // false
console.log(x !== y); // true
console.log(x > y);   // true
console.log(x < y);  // false

let c = true, d = false;
console.log(c && d); // false
console.log(c || d); // true
console.log(!c);     // false